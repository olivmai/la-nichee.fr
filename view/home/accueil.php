  <div class="row accueil-p">
    <p>Au bout d’une allée privative avec jardin clos de 350 m², rénovée et agrandie, cette maison est située dans le Cotentin (département de la Manche) dans le village de Carteret. C’est un bourg de pêcheurs (de crustacés) très agréable. Le port est à trois cents mètres de la maison, la plage à un kilomètre ; c’est une balade très sympathique et animée en été. De la plage on voit l’île de Jersey située à 35 km de là (une liaison maritime Carteret-Jersey est mise en place pendant la saison).</p>
